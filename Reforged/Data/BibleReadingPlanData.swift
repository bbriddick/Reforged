    private static func makePlan(id: String, name: String, description: String,
                                 iconName: String, accentColor: Color,
                                 raw: [(String, String)]) -> BibleReadingPlan {
        let entries = raw.enumerated().map { (i, pair) in
            BiblePlanEntry(planId: id, day: i + 1,
                           scriptureReference: pair.0,
                           refinementPrompt: pair.1)
        }
